package com.mycompany.seletiene;

/**
 *
 * @author santi
 */
public abstract class ItemC {
    private String nombre;
    private int code; 

    public ItemC(String nombre, int code) {
        this.nombre = nombre;
        this.code = code;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the code
     */
    public int getCode() {
        return code;
    }

    /**
     * @param code the code to set
     */
    public void setCode(int code) {
        this.code = code;
    }
    
}
